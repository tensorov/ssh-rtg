// ─── Adversarial: Simulate crash mid-write → config file not corrupted ───────

func TestAtomicWritePowerLoss(t *testing.T) {
	dir := setupTestDir(t)

	// First, write a valid config
	initialServices := []registry.Service{
		{Host: "vps-b", Port: 8080, Domain: "app.zeitoven.ru", Proto: "http", TTL: 30},
	}
	if err := Generate(initialServices, dir); err != nil {
		t.Fatalf("Generate initial: %v", err)
	}

	originalContent := readConfig(t, dir)

	// Simulate power loss: write a .tmp file but don't rename it
	tmpPath := filepath.Join(dir, "rtg-services.yml.tmp")
	corruptContent := "corrupted data that should not end up in the real file"
	if err := os.WriteFile(tmpPath, []byte(corruptContent), 0o644); err != nil {
		t.Fatalf("write tmp: %v", err)
	}

	// Verify the real config file is intact
	currentContent := readConfig(t, dir)
	if currentContent != originalContent {
		t.Error("config file was corrupted by interrupted write")
	}
	if strings.Contains(currentContent, "corrupted") {
		t.Error("config contains corrupted data from tmp file")
	}

	// Clean up: on next successful Generate, tmp is removed and config is updated
	newServices := []registry.Service{
		{Host: "vps-c", Port: 5432, Proto: "tcp", TTL: 30},
	}
	if err := Generate(newServices, dir); err != nil {
		t.Fatalf("Generate after simulated crash: %v", err)
	}

	// Verify old tmp is cleaned up
	if _, err := os.Stat(tmpPath); !os.IsNotExist(err) {
		t.Error(".tmp file should not exist after successful Generate")
	}

	// Verify new config has the TCP service
	content := readConfig(t, dir)
	if !strings.Contains(content, "vps-c-5432") {
		t.Error("expected TCP service after crash recovery")
	}
}

// ─── Adversarial: Parallel Generate calls don't corrupt config ───────────────

func TestGenerateIdempotent(t *testing.T) {
	dir := setupTestDir(t)

	services := []registry.Service{
		{Host: "vps-b", Port: 8080, Domain: "app.zeitoven.ru", Proto: "http", TTL: 30},
	}

	// Generate multiple times — should produce identical output
	if err := Generate(services, dir); err != nil {
		t.Fatalf("Generate #1: %v", err)
	}
	content1 := readConfig(t, dir)

	if err := Generate(services, dir); err != nil {
		t.Fatalf("Generate #2: %v", err)
	}
	content2 := readConfig(t, dir)

	if content1 != content2 {
		t.Error("idempotent Generate calls should produce identical output")
	}
}

// ─── Verify generated YAML is parseable (basic structure) ───────────────────

func TestGeneratedYamlStructure(t *testing.T) {
	dir := setupTestDir(t)

	services := []registry.Service{
		{Host: "vps-b", Port: 8080, Domain: "myapp.zeitoven.ru", Proto: "http", TTL: 30},
		{Host: "vps-c", Port: 5432, Proto: "tcp", TTL: 30},
	}

	if err := Generate(services, dir); err != nil {
		t.Fatalf("Generate: %v", err)
	}

	content := readConfig(t, dir)

	// Check overall structure
	lines := strings.Split(content, "\n")

	// First line should be header
	if !strings.HasPrefix(lines[0], "# Auto-generated") {
		t.Error("first line should be header comment")
	}

	// Check indentation consistency
	for _, line := range lines {
		trimmed := strings.TrimLeft(line, " ")
		indent := len(line) - len(trimmed)
		if indent%2 != 0 {
			t.Errorf("odd indentation at line: %q (indent=%d)", line, indent)
		}
	}
}
