// setupTestDir creates a temporary directory for testing.
func setupTestDir(t *testing.T) string {
	t.Helper()
	return t.TempDir()
}

// readConfig reads the generated config file and returns its content.
func readConfig(t *testing.T, configDir string) string {
	t.Helper()
	configPath := filepath.Join(configDir, "rtg-services.yml")
	data, err := os.ReadFile(configPath)
	if err != nil {
		t.Fatalf("read config: %v", err)
	}
	return string(data)
}

// ─── Happy: HTTP with domain → config contains HTTP router + TLS ─────────────

func TestGenerateHttpWithDomain(t *testing.T) {
	dir := setupTestDir(t)

	services := []registry.Service{
		{Host: "vps-b", Port: 8080, Domain: "myapp.zeitoven.ru", Proto: "http", TTL: 30},
	}

	err := Generate(services, dir)
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}

	content := readConfig(t, dir)

	if !strings.Contains(content, "myapp-zeitoven-ru") {
		t.Error("expected router name 'myapp-zeitoven-ru' in config")
	}
	if !strings.Contains(content, "Host(`myapp.zeitoven.ru`)") {
		t.Error("expected Host rule with domain")
	}
	if !strings.Contains(content, "certResolver: default") {
		t.Error("expected TLS certResolver")
	}
	if !strings.Contains(content, "url: \"http://vps-b:8080\"") {
		t.Error("expected service URL")
	}
}

