// ─── Happy: Delete → router removed (only remaining services) ────────────────

func TestGenerateAfterDelete(t *testing.T) {
	dir := setupTestDir(t)

	// Generate with two services
	services := []registry.Service{
		{Host: "vps-b", Port: 8080, Domain: "app1.zeitoven.ru", Proto: "http", TTL: 30},
		{Host: "vps-c", Port: 5432, Proto: "tcp", TTL: 30},
	}
	if err := Generate(services, dir); err != nil {
		t.Fatalf("Generate initial: %v", err)
	}

	// Generate with only one remaining (simulates delete)
	remaining := []registry.Service{
		{Host: "vps-c", Port: 5432, Proto: "tcp", TTL: 30},
	}
	if err := Generate(remaining, dir); err != nil {
		t.Fatalf("Generate after delete: %v", err)
	}

	content := readConfig(t, dir)

	// vps-c should still be there
	if !strings.Contains(content, "vps-c-5432") {
		t.Error("expected remaining TCP router vps-c-5432")
	}

	// app1 should be gone
	if strings.Contains(content, "app1-zeitoven-ru") {
		t.Error("removed service should not appear in config")
	}
}

