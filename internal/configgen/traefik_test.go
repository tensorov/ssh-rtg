// ─── Happy: Multiple services (HTTP + TCP) in one config ─────────────────────

func TestGenerateMixedServices(t *testing.T) {
	dir := setupTestDir(t)

	services := []registry.Service{
		{Host: "vps-b", Port: 8080, Domain: "app1.zeitoven.ru", Proto: "http", TTL: 30},
		{Host: "vps-c", Port: 5432, Proto: "tcp", TTL: 30},
		{Host: "vps-d", Port: 3306, Proto: "tcp", TTL: 30},
	}

	err := Generate(services, dir)
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}

	content := readConfig(t, dir)

	// Should contain both http and tcp sections
	if !strings.Contains(content, "http:") {
		t.Error("expected http section")
	}
	if !strings.Contains(content, "tcp:") {
		t.Error("expected tcp section")
	}

	// Should contain all three services
	if !strings.Contains(content, "app1-zeitoven-ru") {
		t.Error("expected HTTP router app1-zeitoven-ru")
	}
	if !strings.Contains(content, "vps-c-5432") {
		t.Error("expected TCP router vps-c-5432")
	}
	if !strings.Contains(content, "vps-d-3306") {
		t.Error("expected TCP router vps-d-3306")
	}
}

